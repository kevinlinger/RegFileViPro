use strict;

##############################################
# Clean up current users autogenerated files #
##############################################
# GLOBAL VARIABLES #
####################
my $usr = getlogin();

#################
# Begin Program #
#################
my $path = $0;
$path =~ s/clean.pl//;
if ($path){
    chdir $path;
}

if ($#ARGV){
    print("Cleaning  autogenerated files for current user...\n");
}
system("rm -f ../" . $usr . ".ini");
system("rm -rf " . $usr);
system("rm -rf *~");
if ($#ARGV){
    print("...completed!\n");
}










